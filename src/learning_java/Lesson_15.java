package learning_java;

public class Lesson_15 {
	public static void main(String[] args) {
		String banana = "banana";
		String cucumber= "cucumber" ;
		String together = banana + cucumber;
		
		System.out.println("The length of the banana is " + banana.length());
		System.out.println("The length of the cucumber is " + cucumber.length());
		System.out.println("The word " + together + " length is " + together.length());
				
				
	}
}
