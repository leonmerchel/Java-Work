package learning_java;

public class Assignment_Operations {
	public static void main(String[] args) {
		int length = 100;
		length += 10; //same as length = length + 10;
		System.out.println(length);
		length -= 50; 
		System.out.println(length);
		
		int otherlength = 100;
		length *= 10;
		otherlength /= 100;
		otherlength %= 3;

	}
}
